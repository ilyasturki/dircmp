import type { Key } from 'ink'

import type { Action } from '~/utils/types'

type KeyMatcher = (input: string, key: Key) => boolean

type ShortcutEffect = { type: 'dispatch'; action: Action } | { type: 'exit' }

export interface Shortcut {
    id: string
    mode: 'directoryDiff' | 'fileDiff' | 'universal'
    keyLabel: string
    description: string
    helpDescription: string
    match: KeyMatcher
    effect: ShortcutEffect
    keyDef: string | string[]
    sequence?: string
    helpKey?: string
}

export interface HelpItem {
    key: string
    description: string
    helpDescription: string
    effect: ShortcutEffect
}

export function getHelpItems(shortcuts: Shortcut[]): HelpItem[] {
    return shortcuts
        .filter((s) => s.helpKey !== undefined)
        .map((s) => ({
            key: s.helpKey!,
            description: s.description,
            helpDescription: s.helpDescription,
            effect: s.effect,
        }))
}

